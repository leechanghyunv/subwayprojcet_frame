
import 'package:get/get.dart';

class Inter_Changer  {

  String subway_line = '';

  void convertLine(int number) {
    if (number == 1) {
      subway_line = 'Line1';
    } else if (number == 2) {
      subway_line = 'Line2';
    } else if (number == 3) {
      subway_line = 'Line3';
    } else if (number == 4) {
      subway_line = 'Line4';
    } else if (number == 5) {
      subway_line = 'Line5';
    } else if (number == 6) {
      subway_line = 'Line6';
    } else if (number == 7) {
      subway_line = 'Line7';
    } else if (number == 8) {
      subway_line = 'Line8';
    } else if (number == 9) {
      subway_line = 'Line9';
    } else if (number == 10) {
      subway_line = '신분당';
    } else if (number == 11) {
      subway_line = '수인분당';
    } else if (number == 12) {
      subway_line = '경의중앙';
    }
  }



}